def match_faq(text: str) -> str | None:
    """
    - 完全一致じゃなく「部分一致」にする
    - 同義語も拾う
    - 複数ヒットしたら priority で一番良いものを返す
    """
    t = normalize_text(text)
    if not t:
        return None

    best_key = None
    best_score = 0.0

    for key in FAQ.keys():
        key_1 = normalize_text(key)

        # キー自体の部分一致
        hit = key_1 in t

        # 同義語で探す
        if not hit:
            for syn in FAQ_SYNONYMS.get(key, []):
                if normalize_text(syn) in t:
                    hit = True
                    break
        
        if not hit:
            continue

        # スコア計算
        score = float(FAQ_PRIORITY.get(key, 1))
        score += len(key) * 0.1 #同点対策

        if score > best_score:
            best_score = score
            best_key = key

    return FAQ.get(best_key) if best_key else None