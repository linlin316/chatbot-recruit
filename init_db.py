def seed():
    """FAQデータをDBに投入する"""
    conn = get_db()
    cursor = conn.cursor()

    # すでにデータがある場合はスキップ
    existing = cursor.execute("SELECT COUNT(*) FROM faq").fetchone()[0]
    if existing > 0:
        print(f"すでに {existing} 件のFAQデータがあります。スキップします。")
        print("   再投入したい場合は data/chatbot.db を削除してから実行してください。")
        conn.close()
        return

    for key, synonyms, answer, priority in FAQ_DATA:
        cursor.execute(
            "INSERT INTO faq (key, synonyms, answer, priority) VALUES (?, ?, ?, ?)",
            (key, json.dumps(synonyms, ensure_ascii=False), answer, priority)
        )

    conn.commit()
    conn.close()
    print(f"{len(FAQ_DATA)} 件のFAQデータを投入しました")


if __name__ == "__main__":
    print("DBを初期化しています...")
    init_db()
    print("FAQデータを投入しています...")
    seed()
    print("完了！")